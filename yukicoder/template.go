package main

import "fmt"

func main() {
	fmt.Printf("Hello Go World2")
}

func readLine() string {
	var str string
	fmt.Scan(&str)
	return str
}

func scanNums(len int) (nums []int) {
	var num int
	for i := 0; i < len; i++ {
		fmt.Scan(&num)
		nums = append(nums, num)
	}
	return
}

func scanStrings(len int) (strings []string) {
	var str string
	for i := 0; i < len; i++ {
		fmt.Scanf("%s", &str)
		strings = append(strings, str)
	}
	return
}
